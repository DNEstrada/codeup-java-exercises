public class StringExercise {
    public static void main (String[] args) {
        String brickInTheWall = "We don't need no education\n" + "We don't need no thought control";
        System.out.println(brickInTheWall);
        String quotations = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotations);
        String drive = "In windows, the main drive is usually C:\\";
        System.out.println(drive);
        String slashes = "I can do backslashes \\, double backslashes \\\\,\n" + "and the amazing triple backslash \\\\\\!";
        System.out.println(slashes);
    }
}
